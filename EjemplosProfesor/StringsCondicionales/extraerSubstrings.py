 # vamos a ver substring


texto="ABCDEFGHIJKLMNÑOPQRSTUVWXYZ"
fragmento=texto[2] # desde la posición 0 hasta la 5
print(fragmento)

fragmento=texto[2:5] # desde la posición 2 hasta la 5. no es inclusivo
print(fragmento)

fragmento=texto[2:] # desde la posición 2 hasta el final
print(fragmento)

fragmento=texto[:5] # desde la posición 0 hasta la 5
print(fragmento)

fragmento=texto[2:10:2] # desde la posición 2 hasta la 10 de 2 en 2
print(fragmento)

fragmento=texto[::2] # desde la posición 0 hasta el final de 2 en 2
print(fragmento)

fragmento=texto[::-1] # desde la posición 0 hasta el final de -1 en -1
print(fragmento)
















